"""Детерминированный маршрутизатор по ключевым словам.

Используется в режиме без LLM, а также как запасной вариант, если LLM-классификация
не дала валидного ответа.
"""
from __future__ import annotations

_SLEEP = ("режим сна", "ко сну", "подготовь меня ко сну", "спать", "засыпа",
          "закрой день", "закрой мой день", "заверши день", "доброй ночи",
          "спокойной ночи", "shutdown", "сонная фея")
_DIGEST = ("дайджест", "сводк", "итоги дня", "что по плану на день")
_PRIORITY = ("что мне делать", "что делать дальше", "что сейчас делать",
             "что дальше", "чем заняться", "с чего начать", "какую задачу",
             "приоритет", "что важнее", "что делать", "делать дальше",
             "что мне сейчас", "за что взяться")
_PLANNING_STRONG = ("спланируй", "распредели", "составь план", "распиши по дням",
                    "распланируй", "по дням")
_PLANNING_VERBS = ("подготов", "сделать", "написать", "прочитать", "разбей",
                   "запланир", "нужно", "надо", "задач", "успеть", "дела ")
_MEMORY = ("запомни", "запиши, что", "запиши что", "заметь, что", "забудь",
           "что ты помнишь", "покажи память", "моя память", "что ты обо мне")
_CALENDAR = ("событие", "событий", "встреч", "календар", "расписани",
             "свободн", "конфликт", "во сколько", "перенеси встречу", "слот")
_TASK = ("задач", "task", "чеклист", "разбей", "todo", "to-do", "подзадач",
         "статус", "выполнено", "сделано", "приоритет задачи")


def _has(text: str, words) -> bool:
    return any(w in text for w in words)


def _is_planning(t: str) -> bool:
    if _has(t, _PLANNING_STRONG):
        return True
    # «… за следующую неделю … подготовить/сделать/разбей …»
    if "недел" in t and _has(t, _PLANNING_VERBS):
        return True
    return False


def keyword_route(message: str) -> str:
    t = message.lower()
    # Порядок важен: более специфичные интенты проверяем раньше.
    if _has(t, _SLEEP):
        return "sleep_fairy"
    if _is_planning(t):
        return "planning"
    if _has(t, _PRIORITY):
        return "prioritization"
    if _has(t, _DIGEST):
        return "digest"
    if _has(t, _MEMORY):
        return "memory"
    if _has(t, _CALENDAR):
        return "calendar"
    if _has(t, _TASK):
        return "task"
    return "task"  # разумный дефолт
