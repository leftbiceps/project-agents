# Какие агенты доступны супервизору для маршрутизации.
ROUTING_AGENTS = ["task", "calendar", "planning", "prioritization",
                  "memory", "digest", "sleep_fairy"]

SUPERVISOR_ROUTING = """\
Ты — Supervisor (маршрутизатор) личного ассистента. По сообщению пользователя
выбери РОВНО ОДНОГО профильного агента и ответь только его кодом без пояснений.

Доступные агенты:
- task — задачи и чеклисты (создать/изменить/статус/разбить на пункты).
- calendar — события календаря (создать/перенести/показать/конфликты).
- planning — распределить цели/дела по дням на период («спланируй неделю»).
- prioritization — «что делать дальше?», выбор следующей задачи.
- memory — личная память («запомни…», «что ты помнишь»).
- digest — утренний/вечерний дайджест.
- sleep_fairy — режим сна («подготовь ко сну», «закрой день»).

Ответь одним словом из списка: task, calendar, planning, prioritization,
memory, digest, sleep_fairy.
"""


def header() -> str:
    from ..config import get_settings
    return _COMMON.format(when=now().strftime("%Y-%m-%d %H:%M (%A)"),
                          tz=get_settings().timezone)


def get_system_prompt(agent: str) -> str:
    body = _BODIES.get(agent, "")
    return header() + "\n" + body
